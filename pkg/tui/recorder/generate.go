// keyCall renders a non-typing key press as a Press/Enter DSL call and
// reports whether the generated code references the tea package.
func keyCall(k tea.KeyPressMsg) (call string, needsTea bool) {
	if k.Code == tea.KeyEnter && k.Mod == 0 {
		return "Enter()", false
	}

	var mods []string
	for _, m := range []struct {
		bit  tea.KeyMod
		name string
	}{
		{tea.ModShift, "tea.ModShift"},
		{tea.ModAlt, "tea.ModAlt"},
		{tea.ModCtrl, "tea.ModCtrl"},
		{tea.ModMeta, "tea.ModMeta"},
		{tea.ModSuper, "tea.ModSuper"},
		{tea.ModHyper, "tea.ModHyper"},
	} {
		if k.Mod&m.bit != 0 {
			mods = append(mods, m.name)
		}
	}
	modArgs := ""
	if len(mods) > 0 {
		modArgs = ", " + strings.Join(mods, ", ")
	}

	if name, ok := keyNames[k.Code]; ok {
		return fmt.Sprintf("Press(%s%s)", name, modArgs), true
	}
	return fmt.Sprintf("Press(%q%s)", k.Code, modArgs), len(mods) > 0
}

// clickCall renders a mouse click. When the text under the cursor is unique
// in the frame it clicks by text (resilient to layout shifts); otherwise it
// falls back to raw coordinates.
func clickCall(e event) string {
	if word := wordAt(e.frame, e.x, e.y); word != "" && strings.Count(e.frame, word) == 1 {
		return fmt.Sprintf("ClickText(%q)", word)
	}
	return fmt.Sprintf("Click(%d, %d)", e.x, e.y)
}

// wordAt returns the space-delimited word covering display column x on line y
// of frame, or "" if the position is blank or out of bounds. x is a display
// width, so wide Unicode before the click is handled.
func wordAt(frame string, x, y int) string {
	lines := strings.Split(frame, "\n")
	if y < 0 || y >= len(lines) {
		return ""
	}
	runes := []rune(lines[y])

	idx := -1
	col := 0
	for i, r := range runes {
		w := runewidth.RuneWidth(r)
		if x < col+w {
			idx = i
			break
		}
		col += w
	}
	if idx < 0 || runes[idx] == ' ' {
		return ""
	}

	lo, hi := idx, idx
	for lo > 0 && runes[lo-1] != ' ' {
		lo--
	}
	for hi < len(runes)-1 && runes[hi+1] != ' ' {
		hi++
	}
	return string(runes[lo : hi+1])
}

// borderCutset is trimmed off frame lines before they are considered as
// WaitFor anchors, so box-drawing chrome never anchors a wait.
const borderCutset = " \t│┃║╭╮╰╯└┘┌┐├┤┬┴─━┄┅┆┇┈┉"

// diffAnchor picks the most useful line that is present in next but not in
// prev — the longest new line, which biases toward real message content over
// status-bar churn. The result is capped so goldens stay readable.
func diffAnchor(prev, next string) string {
	prevLines := make(map[string]bool)
	for line := range strings.SplitSeq(prev, "\n") {
		prevLines[strings.Trim(line, borderCutset)] = true
	}

	best := ""
	for line := range strings.SplitSeq(next, "\n") {
		s := strings.Trim(line, borderCutset)
		if utf8.RuneCountInString(s) < 4 || prevLines[s] || hasSpinner(s) {
			continue
		}
		if utf8.RuneCountInString(s) > utf8.RuneCountInString(best) {
			best = s
		}
	}
	return truncateRunes(best, maxAnchorRunes)
}

// hasSpinner reports whether s contains braille spinner glyphs, which animate
// between frames and would make an anchor flaky.
func hasSpinner(s string) bool {
	for _, r := range s {
		if r >= 0x2800 && r <= 0x28FF {
			return true
		}
	}
	return false
}

func truncateRunes(s string, n int) string {
	runes := []rune(s)
	if len(runes) <= n {
		return s
	}
	return strings.TrimRight(string(runes[:n]), " ")
}
